from datetime import datetime
from typing import Sequence

import polars as pl
from tqdm import tqdm

from .victoria_download import download_metrics
from .data_types import Experiment, ExperimentWithResults

ALL_METRICS = [
    "ss_snd_cwnd",
    "ss_snd_ssthresh",
    "ss_bytes_sent",
    "ss_bytes_retrans",
    "ss_bytes_acked",
    "ss_delivery_rate",
    "ss_rtt",
    "ss_rttvar",
    # "ss_number_of_samples",
    "iperf_bytes",
    "iperf_bits_per_second",
    "iperf_retransmits",
    "iperf_snd_cwnd",
    "iperf_snd_wnd",
    "iperf_rtt",
    "iperf_rttvar",
    "iperf_pmtu",
    "hfe_number_of_benchmarks",
]


def load_experiments_from_csv(file_path: str) -> list[Experiment]:
    """
    Loads the list of experiments from a CSV file.

    This CSV file is generated by the serial experiments runner.
    """
    df = pl.read_csv(file_path)
    experiments: list[Experiment] = []

    experiments_scenarios = set()
    for row in df.iter_rows(named=True):
        if row["Scenario"] in experiments_scenarios:
            raise ValueError(
                f"At least one duplicate scenario found in results.csv: {row['Scenario']}"
            )

        experiments_scenarios.add(row["Scenario"])

        experiment: Experiment = {
            "scenario": row["Scenario"],
            "start_time": datetime.fromisoformat(row["Launch time"]),
            "end_time": datetime.fromisoformat(row["End time"]),
            "description": row["Description"],
        }

        experiments.append(experiment)

    return experiments


def experiments_download(
    experiments: Sequence[Experiment], metrics: list[str] = ALL_METRICS
) -> list[ExperimentWithResults]:
    """
    Downloads the metrics for each experiment and returns a list of experiments with their results.
    """

    results: list[ExperimentWithResults] = []
    for experiment in tqdm(experiments, leave=False):
        df = download_metrics(experiment["start_time"], experiment["end_time"], metrics)

        result: ExperimentWithResults = {
            "scenario": experiment["scenario"],
            "start_time": experiment["start_time"],
            "end_time": experiment["end_time"],
            "description": experiment["description"],
            "results": df,
        }

        results.append(result)

    return results
